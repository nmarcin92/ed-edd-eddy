drop table if exists category_keyword, tweet, category, keyword, sentiment, twitter_user, keyword_tweet, sentiment_tweet, mention, hashtag, hashtag_tweet cascade;

CREATE TABLE "category" (
  "id" SERIAL PRIMARY KEY,
  "category" TEXT NOT NULL
);

CREATE TABLE "keyword" (
  "id" SERIAL PRIMARY KEY,
  "word" TEXT NOT NULL
);

CREATE TABLE "hashtag" (
  "id" SERIAL PRIMARY KEY,
  "text" TEXT NOT NULL UNIQUE
);

CREATE TABLE "category_keyword" (
  "category" INTEGER NOT NULL,
  "keyword" INTEGER NOT NULL,
  PRIMARY KEY ("category", "keyword")
);

CREATE INDEX "idx_category_keyword" ON "category_keyword" ("keyword");

ALTER TABLE "category_keyword" ADD CONSTRAINT "fk_category_keyword__category" FOREIGN KEY ("category") REFERENCES "category" ("id");

ALTER TABLE "category_keyword" ADD CONSTRAINT "fk_category_keyword__keyword" FOREIGN KEY ("keyword") REFERENCES "keyword" ("id");

CREATE TABLE "sentiment" (
  "id" SERIAL PRIMARY KEY,
  "word" TEXT NOT NULL,
  "positive_score" DECIMAL(12, 2) NOT NULL,
  "negative_score" DECIMAL(12, 2) NOT NULL
);

CREATE TABLE "twitter_user" (
  "id" TEXT PRIMARY KEY,
  "username" TEXT NOT NULL,
  "followers_count" INTEGER,
  "friends_count" INTEGER,
  "statuses_count" INTEGER,
  "created_date" TIMESTAMP
);

CREATE TABLE "tweet" (
  "id" TEXT PRIMARY KEY,
  "user_id" TEXT NOT NULL,
  "text" TEXT NOT NULL,
  "localization" TEXT,
  "date" TIMESTAMP NOT NULL,
  "retweet_count" INTEGER NOT NULL,
  "in_reply_to" TEXT,
  "retweet_id" TEXT,
  "category" INTEGER,
  "sentiment" TEXT
);

CREATE INDEX "idx_tweet__category" ON "tweet" ("category");

CREATE INDEX "idx_tweet__user" ON "tweet" ("user_id");

ALTER TABLE "tweet" ADD CONSTRAINT "fk_tweet__category" FOREIGN KEY ("category") REFERENCES "category" ("id");

ALTER TABLE "tweet" ADD CONSTRAINT "fk_tweet__user" FOREIGN KEY ("user_id") REFERENCES "twitter_user" ("id");

CREATE TABLE "keyword_tweet" (
  "keyword" INTEGER NOT NULL,
  "tweet" TEXT NOT NULL,
  PRIMARY KEY ("keyword", "tweet")
);

CREATE INDEX "idx_keyword_tweet" ON "keyword_tweet" ("tweet");

ALTER TABLE "keyword_tweet" ADD CONSTRAINT "fk_keyword_tweet__keyword" FOREIGN KEY ("keyword") REFERENCES "keyword" ("id");

ALTER TABLE "keyword_tweet" ADD CONSTRAINT "fk_keyword_tweet__tweet" FOREIGN KEY ("tweet") REFERENCES "tweet" ("id");

CREATE TABLE "sentiment_tweet" (
  "sentiment" INTEGER NOT NULL,
  "tweet" TEXT NOT NULL,
  PRIMARY KEY ("sentiment", "tweet")
);

CREATE INDEX "idx_sentiment_tweet" ON "sentiment_tweet" ("tweet");

ALTER TABLE "sentiment_tweet" ADD CONSTRAINT "fk_sentiment_tweet__sentiment" FOREIGN KEY ("sentiment") REFERENCES "sentiment" ("id");

ALTER TABLE "sentiment_tweet" ADD CONSTRAINT "fk_sentiment_tweet__tweet" FOREIGN KEY ("tweet") REFERENCES "tweet" ("id");

CREATE TABLE "mention" (
  "tweet" TEXT NOT NULL,
  "user" TEXT NOT NULL,
  PRIMARY KEY ("tweet", "user")
);

CREATE INDEX "idx_mention" ON "mention" ("user");

ALTER TABLE "mention" ADD CONSTRAINT "fk_mention_tweet" FOREIGN KEY ("tweet") REFERENCES "tweet" ("id");

ALTER TABLE "mention" ADD CONSTRAINT "fk_mention_user" FOREIGN KEY ("user") REFERENCES "twitter_user" ("id");

CREATE TABLE "hashtag_tweet" (
  "hashtag" INTEGER NOT NULL,
  "tweet" TEXT NOT NULL,
  PRIMARY KEY ("hashtag", "tweet")
);

CREATE INDEX "idx_hashtag_tweet" ON "hashtag_tweet" ("tweet");

ALTER TABLE "hashtag_tweet" ADD CONSTRAINT "fk_hashtag_tweet_hashtag" FOREIGN KEY ("hashtag") REFERENCES "hashtag" ("id");

ALTER TABLE "hashtag_tweet" ADD CONSTRAINT "fk_hashtag_tweet_tweet" FOREIGN KEY ("tweet") REFERENCES "tweet" ("id");


